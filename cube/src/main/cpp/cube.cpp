void Cube::Update(float delta_time) {

}

void Cube::Draw(const glm::mat4 &projection_matrix,
                const glm::mat4 &view_matrix) {
    shader_.Use();

    glActiveTexture(GL_TEXTURE0);
    texture1_.Bind();
    glActiveTexture(GL_TEXTURE1);
    texture2_.Bind();

    shader_.SetInteger("texture1", 0);
    shader_.SetInteger("texture2", 1);

    shader_.SetMatrix4("model", model_matrix_);
    shader_.SetMatrix4("projection", projection_matrix);
    shader_.SetMatrix4("view", view_matrix);

    glBindVertexArray(vao_);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

